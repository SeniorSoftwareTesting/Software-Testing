public class Calculator {
    public static int adunare(int a, int b){
        return a + b;
    }
    public static int scaderea(int q, int w){
        return q - w;
    }
    public static int inmultire(int c, int d){
        return c * d;
    }
    public static int impartire(int m, int n){
        return m / n;
    }
}
